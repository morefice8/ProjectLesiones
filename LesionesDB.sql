create database LESIONESDB;

use LESIONESDB;

create table jugador (
	dni_jugador varchar(10),
	nombre_jugador varchar(50),
	fecha_nacimiento_jugador date,
	primary key (dni_jugador)
);

create table especialista (
	dni_especialista varchar(10),
	nombre_especialista varchar(30),
	especialidad varchar(30),
	primary key (dni_especialista)
);

create table lesion (
	id_lesion int,
	nombre_lesion varchar(50),
	explicacion_lesion varchar(100),
	primary key (id_lesion)
);

create table tratamiento (
	id_tratamiento int,
	nombre_tratamiento varchar(50),
	explicacion_tratamiento varchar(100),
	primary key (id_tratamiento)
);


create table jugador_lesion (
	dni_jugador varchar(10),
	id_lesion int,
	fecha_lesion date,
	primary key (dni_jugador, id_lesion, fecha_lesion),
	foreign key (dni_jugador) references jugador (dni_jugador),
	foreign key (id_lesion) references lesion (id_lesion)
);

create table lesion_tratamiento (
	id_lesion int,
	id_tratamiento int,
	primary key (id_lesion, id_tratamiento),
	foreign key (id_lesion) references lesion (id_lesion),
	foreign key (id_tratamiento) references tratamiento (id_tratamiento)
);

create table tratamiento_especialista (
	id_tratamiento int,
	dni_especialista varchar(10),
	primary key (id_tratamiento, dni_especialista),
	foreign key (id_tratamiento) references tratamiento (id_tratamiento),
	foreign key (dni_especialista) references especialista (dni_especialista)
);

create table jugador_lesion_tratamiento_especialista (
	dni_jugador varchar(10),
	id_lesion int,
	fecha_lesion date,
	id_tratamiento int,
	dni_especialista varchar(10),
	primary key (dni_jugador, id_lesion, fecha_lesion, id_tratamiento, dni_especialista),
	foreign key (dni_jugador, id_lesion, fecha_lesion) references jugador_lesion (dni_jugador, id_lesion, fecha_lesion),
	foreign key (id_tratamiento, dni_especialista) references tratamiento_especialista (id_tratamiento, dni_especialista)
);


